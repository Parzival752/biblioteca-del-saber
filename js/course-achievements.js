function buildModuleBadges(courseId) {
  const modules = getCurriculum(courseId) || [];
  return modules.map((mod, index) => ({
    id: `${courseId}-mod-${index + 1}`,
    icon: ['📘', '📗', '📙', '📕', '📓'][index % 5],
    name: `Módulo: ${mod.title}`,
    desc: `Completa las ${mod.lessons.length} lecciones de «${mod.title}»`,
    metric: 'module',
    moduleIndex: index,
    target: mod.lessons.length,
    group: 'module',
    courseId,
  }));
}

function buildMilestoneBadges(courseId) {
  const list = COURSE_MILESTONES[courseId] || [];
  return list.map((item, i) => ({
    id: `${courseId}-ms-${i + 1}`,
    icon: item.icon,
    name: item.name,
    desc: item.desc,
    metric: 'lessons',
    target: item.at,
    group: 'course',
    courseId,
  }));
}

function buildSpecialBadges(courseId) {
  const list = COURSE_SPECIALS[courseId] || [];
  return list.map((item, i) => ({
    id: `${courseId}-sp-${i + 1}`,
    icon: item.icon,
    name: item.name,
    desc: item.desc,
    metric: item.metric,
    target: item.target,
    group: 'course',
    courseId,
  }));
}

/** Logros exclusivos del saber (módulos + hitos + especiales). */
export function getCourseSpecificBadges(courseId) {
  const total = getLessonCount(courseId);
  if (total < FULL_ACHIEVEMENT_MIN_LESSONS) return [];
  if (!COURSE_MILESTONES[courseId]) return [];
  return [
    ...buildModuleBadges(courseId),
    ...buildMilestoneBadges(courseId),
    ...buildSpecialBadges(courseId),
  ];
}

export function hasFullAchievementPack(courseId) {
  return getLessonCount(courseId) >= FULL_ACHIEVEMENT_MIN_LESSONS
    && Boolean(COURSE_MILESTONES[courseId]);
}
